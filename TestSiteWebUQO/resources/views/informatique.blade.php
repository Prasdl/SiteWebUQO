    <h2>Qu'est-ce que l'informatique ?</h2>

    <p>L'informatique a pour objet la théorie, la conception et le développement des systèmes informatiques et des logiciels.
        Elle comprend les mathématiques liées à l'informatique, la méthodologie (langages et systèmes) de programmation,
        les architectures logicielles et de systèmes, ainsi qu'une vaste gamme de domaines d'application.</p>

    <h2>Emplois pour l'étudiant titulaire d'un diplôme en informatique</h2>
    <p>De façon générale, le travail de l'informaticien consiste à structurer et à représenter l'information,
        et à en faciliter l'utilisation et la transmission. À travers sa formation académique, l'étudiant développera en particulier les connaissances, capacités et qualités suivantes :</p>
    <ul>
        <li><p>comprendre et appliquer le processus d'ingénierie du logiciel;</p></li>
        <li><p>structurer, planifier et gérer des projets informatiques;</p></li>
        <li><p>développer des logiciels de qualité tout en respectant les budgets et les échéances;</p></li>
        <li><p>mettre en exploitation et exploiter des systèmes informatisés;</p></li>
        <li><p>s'adapter à l'évolution rapide des technologies de l'information;</p></li>
        <li><p>travailler efficacement en équipe multidisciplinaire tout en respectant les règles de la profession;</p></li>
        <li><p>communiquer efficacement tant à l'oral qu'à l'écrit;</p></li>
        <li><p>comprendre, analyser et définir la part des technologies de l'information dans un processus scientifique ou technique;</p></li>
        <li><p>sélectionner et intégrer à des processus scientifiques ou techniques des solutions existantes issues de technologies de l'information;</p></li>
        <li><p>spécifier, concevoir et mettre en oeuvre des nouvelles solutions mariant les technologies de l'information aux disciplines scientifiques et techniques.</p></li>
    </ul>

